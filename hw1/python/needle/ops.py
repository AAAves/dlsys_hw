class Transpose(TensorOp):
    def __init__(self, axes: Optional[tuple] = None):
        self.axes = axes

    def compute(self, a):
        ### BEGIN YOUR SOLUTION
        dim_array = list(range(a.ndim))
        if self.axes == None:
            dim_array[-1], dim_array[-2] = dim_array[-2], dim_array[-1]
        else:
            dim_array[self.axes[0]], dim_array[self.axes[1]] = dim_array[self.axes[1]], dim_array[self.axes[0]]
        return a.transpose(dim_array)
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        return transpose(out_grad, self.axes)
        ### END YOUR SOLUTION


def transpose(a, axes=None):
    return Transpose(axes)(a)


